    fn resultListener(_: *river.LibinputResultV1, event: river.LibinputResultV1.Event, result: *Result) void {
        const self = result.runtime;
        const status: policy.ResultStatus = switch (event) {
            .success => .success,
            .unsupported => .unsupported,
            .invalid => .invalid,
        };
        const target: policy.ResultTarget = switch (result.target) {
            .device => |id| .{ .device = id },
            .accel_config => |config| .{ .accel_config = config.id },
        };
        self.recordEvent(.{ .libinput_result = .{
            .target = target,
            .operation = result.operation,
            .status = status,
        } });
        switch (result.target) {
            .accel_config => |config| config.pending_results -= 1,
            .device => {},
        }
        for (self.results.items, 0..) |candidate, index| {
            if (candidate != result) continue;
            _ = self.results.orderedRemove(index);
            break;
        }
        result.object.destroy();
        self.allocator.destroy(result);
    }

    fn syncListener(callback: *wl.Callback, event: wl.Callback.Event, self: *Runtime) void {
        switch (event) {
            .done => {
                if (self.sync_callback == callback) self.sync_callback = null;
                callback.destroy();
                if (self.stopping or self.fatal) return;
                self.processUpdate() catch {
                    self.fatal = true;
                };
            },
        }
    }
};

pub fn run(allocator: std.mem.Allocator, loop: *event_loop.EventLoop, host: *policy.Host) !void {
    var runtime: Runtime = undefined;
    try runtime.init(allocator, host);
    defer runtime.deinit();
    defer host.unbindInvalidator();

    try loop.setWayland(.{
        .fd = runtime.display.getFd(),
        .ctx = &runtime,
        .prepare = Runtime.eventLoopPrepare,
        .finish = Runtime.eventLoopFinish,
    });
    defer loop.clearWayland();
    try loop.run();
    runtime.stop();
    if (runtime.fatal) return error.RiverInputManagerFailed;
    if (!runtime.allFinished()) return error.RiverInputShutdownFailed;
}

pub fn validateScalarCommand(command: policy.Command) !void {
    switch (command) {
        .set_repeat_info => |value| if (value.rate < 0 or value.delay < 0) return error.InvalidRepeatInfo,
        .set_scroll_factor => |value| {
            const maximum = @as(f64, @floatFromInt(std.math.maxInt(i32))) / 256.0;
            if (!std.math.isFinite(value.factor) or value.factor < 0 or value.factor > maximum)
                return error.InvalidScrollFactor;
        },
        .map_to_rectangle => |value| if (value.width < 0 or value.height < 0) return error.InvalidRectangle,
        .set_accel_speed => |value| if (!std.math.isFinite(value.speed) or value.speed < -1 or value.speed > 1)
            return error.InvalidAccelSpeed,
        .set_rotation => |value| if (value.angle >= 360) return error.InvalidRotation,
        .set_accel_points => |value| {
            if (!std.math.isFinite(value.step) or value.step <= 0 or value.points.len == 0)
                return error.InvalidAccelPoints;
            for (value.points) |point| if (!std.math.isFinite(point)) return error.InvalidAccelPoints;
        },
        .set_calibration_matrix => |value| for (value.matrix) |number| if (!std.math.isFinite(number))
            return error.InvalidCalibrationMatrix,
        else => {},
    }
}

fn validateWaylandString(value: []const u8) !void {
    if (std.mem.indexOfScalar(u8, value, 0) != null) return error.InvalidWaylandString;
}

fn validateIdentifier(value: []const u8) !void {
    if (value.len == 0 or std.mem.indexOfScalar(u8, value, 0) != null) return error.InvalidIdentifier;
}

fn validateAccelConfigCommand(
    configs: *std.StringHashMapUnmanaged(usize),
    allocator: std.mem.Allocator,
    command: policy.Command,
) !void {
    switch (command) {
        .create_accel_config => |value| {
            try validateIdentifier(value.id);
            if (configs.contains(value.id)) return error.DuplicateAccelConfig;
            try configs.put(allocator, value.id, 0);
        },
        .destroy_accel_config => |value| {
            if ((configs.get(value.id) orelse return error.UnknownAccelConfig) != 0)
                return error.AccelConfigInUse;
            _ = configs.remove(value.id);
        },
        .set_accel_points => |value| {
            const pending = configs.getPtr(value.id) orelse return error.UnknownAccelConfig;
            pending.* += 1;
        },
        .apply_accel_config => |value| {
            if (!configs.contains(value.id)) return error.UnknownAccelConfig;
        },
        else => {},
    }
}

