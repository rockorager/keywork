    fn keyboardListener(_: *river.XkbKeyboardV1, event: river.XkbKeyboardV1.Event, keyboard: *Keyboard) void {
        const self = keyboard.runtime;
        switch (event) {
            .removed => {
                keyboard.removed = true;
                if (keyboard.device) |device| self.recordStateChanged(device.id);
            },
            .input_device => |value| {
                const object = value.device orelse return fatal(self);
                const device = self.findDevice(object) orelse return fatal(self);
                if (device.keyboard != null) return fatal(self);
                keyboard.device = device;
                device.keyboard = keyboard;
                self.recordStateChanged(device.id);
            },
            .layout => |value| {
                const device = keyboard.device orelse return fatal(self);
                const replacement = if (value.name) |name|
                    self.allocator.dupe(u8, std.mem.span(name)) catch return fatal(self)
                else
                    null;
                if (keyboard.layout_name) |name| self.allocator.free(name);
                keyboard.layout_name = replacement;
                keyboard.state.layout_index = value.index;
                keyboard.state.layout_name = replacement;
                self.recordStateChanged(device.id);
            },
            .capslock_enabled, .capslock_disabled => {
                const device = keyboard.device orelse return fatal(self);
                keyboard.state.capslock = switch (event) {
                    .capslock_enabled => true,
                    .capslock_disabled => false,
                    else => unreachable,
                };
                self.recordStateChanged(device.id);
            },
            .numlock_enabled, .numlock_disabled => {
                const device = keyboard.device orelse return fatal(self);
                keyboard.state.numlock = switch (event) {
                    .numlock_enabled => true,
                    .numlock_disabled => false,
                    else => unreachable,
                };
                self.recordStateChanged(device.id);
            },
        }
    }

