//! River input-management, libinput-config, and XKB-config client runtime.

const std = @import("std");
const wayland = @import("wayland");

const event_loop = @import("../linux/event_loop.zig");
const policy = @import("river_input_policy.zig");

const linux = std.os.linux;
const wl = wayland.client.wl;
const river = wayland.client.river;

const log = std.log.scoped(.river_input_manager);

const Device = struct {
    runtime: *Runtime,
    id: u32,
    object: *river.InputDeviceV1,
    type: ?policy.DeviceType = null,
    name: ?[]u8 = null,
    libinput: ?*LibinputDevice = null,
    keyboard: ?*Keyboard = null,
    removed: bool = false,
};

const LibinputDevice = struct {
    runtime: *Runtime,
    object: *river.LibinputDeviceV1,
    device: ?*Device = null,
    state: policy.LibinputState = .{},
    removed: bool = false,
};

const Keyboard = struct {
    runtime: *Runtime,
    object: *river.XkbKeyboardV1,
    device: ?*Device = null,
    state: policy.KeyboardState = .{},
    layout_name: ?[]u8 = null,
    removed: bool = false,
};

const Output = struct {
    runtime: *Runtime,
    id: u32,
    registry_name: u32,
    object: *wl.Output,
    name: ?[]u8 = null,
    removed: bool = false,
};

const Keymap = struct {
    runtime: *Runtime,
    object: *river.XkbKeymapV1,
    id: []u8,
    state: policy.KeymapState = .pending,
    error_message: ?[]u8 = null,
};

const AccelConfig = struct {
    runtime: *Runtime,
    object: *river.LibinputAccelConfigV1,
    id: []u8,
    profile: policy.AccelProfile,
    pending_results: usize = 0,
};

const ResultTarget = union(enum) {
    device: u32,
    accel_config: *AccelConfig,
};

const Result = struct {
    runtime: *Runtime,
    object: *river.LibinputResultV1,
    target: ResultTarget,
    operation: policy.Operation,
};

const Runtime = struct {
    allocator: std.mem.Allocator,
    host: *policy.Host,
    display: *wl.Display,
    registry: *wl.Registry,
    input_manager: ?*river.InputManagerV1 = null,
    libinput_config: ?*river.LibinputConfigV1 = null,
    xkb_config: ?*river.XkbConfigV1 = null,
    input_global: ?u32 = null,
    libinput_global: ?u32 = null,
    xkb_global: ?u32 = null,
    devices: std.ArrayList(*Device) = .empty,
    libinput_devices: std.ArrayList(*LibinputDevice) = .empty,
    keyboards: std.ArrayList(*Keyboard) = .empty,
    outputs: std.ArrayList(*Output) = .empty,
    keymaps: std.ArrayList(*Keymap) = .empty,
    accel_configs: std.ArrayList(*AccelConfig) = .empty,
    results: std.ArrayList(*Result) = .empty,
    events: std.ArrayList(policy.Event) = .empty,
    sync_callback: ?*wl.Callback = null,
    next_id: u32 = 1,
    initializing: bool = true,
    stopping: bool = false,
    input_finished: bool = false,
    libinput_finished: bool = false,
    xkb_finished: bool = false,
    fatal: bool = false,

    fn init(self: *Runtime, allocator: std.mem.Allocator, host: *policy.Host) !void {
        const display = try wl.Display.connect(null);
        errdefer display.disconnect();
        const registry = try display.getRegistry();
        errdefer registry.destroy();

        self.* = .{
            .allocator = allocator,
            .host = host,
            .display = display,
            .registry = registry,
        };
        errdefer self.destroyChildren(false);
        errdefer self.events.deinit(self.allocator);
        registry.setListener(*Runtime, registryListener, self);
        try host.bindInvalidator(self, Runtime.invalidate);
        errdefer host.unbindInvalidator();

        // The first roundtrip discovers globals. The second includes all initial
        // events generated by the bind requests made while dispatching the first.
        if (display.roundtrip() != .SUCCESS) return error.RoundtripFailed;
        if (self.input_manager == null) return error.NoRiverInputManager;
        if (self.libinput_config == null) return error.NoRiverLibinputConfig;
        if (self.xkb_config == null) return error.NoRiverXkbConfig;
        if (self.fatal) return error.RiverInputInitializationFailed;
        if (display.roundtrip() != .SUCCESS) return error.RoundtripFailed;
        if (self.fatal) return error.RiverInputInitializationFailed;
        self.initializing = false;
        try self.processUpdate();
    }

    fn deinit(self: *Runtime) void {
        if (self.sync_callback) |callback| callback.destroy();
        const can_destroy_river = self.allFinished();
        self.destroyChildren(can_destroy_river);
        if (can_destroy_river) {
            self.input_manager.?.destroy();
            self.libinput_config.?.destroy();
            self.xkb_config.?.destroy();
        }
        self.registry.destroy();
        self.display.disconnect();
        self.events.deinit(self.allocator);
    }

    fn destroyChildren(self: *Runtime, destroy_proxies: bool) void {
        for (self.results.items) |result| {
            switch (result.target) {
                .accel_config => |config| config.pending_results -= 1,
                .device => {},
            }
            if (destroy_proxies) result.object.destroy();
            self.allocator.destroy(result);
        }
        self.results.deinit(self.allocator);
        for (self.keymaps.items) |keymap| self.destroyKeymap(keymap, destroy_proxies);
        self.keymaps.deinit(self.allocator);
        for (self.accel_configs.items) |config| self.destroyAccelConfig(config, destroy_proxies);
        self.accel_configs.deinit(self.allocator);
        for (self.keyboards.items) |keyboard| self.destroyKeyboard(keyboard, destroy_proxies);
        self.keyboards.deinit(self.allocator);
        for (self.libinput_devices.items) |device| self.destroyLibinputDevice(device, destroy_proxies);
        self.libinput_devices.deinit(self.allocator);
        for (self.devices.items) |device| self.destroyDevice(device, destroy_proxies);
        self.devices.deinit(self.allocator);
        for (self.outputs.items) |output| self.destroyOutput(output, destroy_proxies);
        self.outputs.deinit(self.allocator);
    }

    fn allocateId(self: *Runtime) !u32 {
        if (self.next_id == std.math.maxInt(u32)) return error.RiverObjectIdExhausted;
        const id = self.next_id;
        self.next_id += 1;
        return id;
    }

    fn allFinished(self: *const Runtime) bool {
        return self.input_finished and self.libinput_finished and self.xkb_finished;
    }

    fn shouldStop(self: *const Runtime) bool {
        return self.fatal or (!self.stopping and
            (self.input_finished or self.libinput_finished or self.xkb_finished));
    }

    fn stop(self: *Runtime) void {
        if (self.stopping) return;
        self.stopping = true;
        if (!self.input_finished) self.input_manager.?.stop();
        if (!self.libinput_finished) self.libinput_config.?.stop();
        if (!self.xkb_finished) self.xkb_config.?.stop();
        while (!self.allFinished()) {
            if (self.display.dispatch() != .SUCCESS) return;
        }
    }

    fn invalidate(ctx: *anyopaque) anyerror!void {
        const self: *Runtime = @ptrCast(@alignCast(ctx));
        if (!self.stopping and !self.fatal) try self.scheduleUpdate();
    }

    fn scheduleUpdate(self: *Runtime) !void {
        if (self.initializing or self.stopping or self.sync_callback != null) return;
        const callback = try self.display.sync();
        self.sync_callback = callback;
        callback.setListener(*Runtime, syncListener, self);
    }

    fn recordEvent(self: *Runtime, event: policy.Event) void {
        self.events.append(self.allocator, event) catch {
            self.fatal = true;
            return;
        };
        self.scheduleUpdate() catch {
            self.fatal = true;
        };
    }

    fn recordStateChanged(self: *Runtime, id: u32) void {
        for (self.events.items) |event| switch (event) {
            .state_changed => |existing| if (existing == id) return,
            else => {},
        };
        self.recordEvent(.{ .state_changed = id });
    }

    fn processUpdate(self: *Runtime) !void {
        if (self.fatal or self.stopping) return;
        self.host.refresh() catch |err| {
            // ensureLoaded keeps the previous successfully loaded root committed.
            log.err("failed to refresh River input policy; keeping the previous policy: {}", .{err});
        };

        var arena = std.heap.ArenaAllocator.init(self.allocator);
        defer arena.deinit();
        const allocator = arena.allocator();
        const context = try self.buildContext(allocator);
        const commands = self.host.update(allocator, context) catch |err| {
            log.err("River input callback failed; applying no changes: {}", .{err});
            self.events.clearRetainingCapacity();
            self.sweepRemoved();
            return;
        };
        self.validateCommands(allocator, commands) catch |err| {
            log.err("River input commands are invalid; applying no changes: {}", .{err});
            self.events.clearRetainingCapacity();
            self.sweepRemoved();
            return;
        };
        // Event payloads may borrow keymap/config strings destroyed by commands.
        self.events.clearRetainingCapacity();
        try self.applyCommands(commands);
        self.sweepRemoved();
    }

    fn buildContext(self: *Runtime, allocator: std.mem.Allocator) !policy.Context {
        var device_count: usize = 0;
        for (self.devices.items) |device| if (!device.removed) {
            device_count += 1;
        };
        const devices = try allocator.alloc(policy.Device, device_count);
        var device_index: usize = 0;
        for (self.devices.items) |device| {
            if (device.removed) continue;
            devices[device_index] = .{
                .id = device.id,
                .type = device.type,
                .name = device.name,
                .libinput = if (device.libinput) |value| if (!value.removed) value.state else null else null,
                .keyboard = if (device.keyboard) |value| if (!value.removed) value.state else null else null,
            };
            device_index += 1;
        }

        var output_count: usize = 0;
        for (self.outputs.items) |output| if (!output.removed) {
            output_count += 1;
        };
        const outputs = try allocator.alloc(policy.Output, output_count);
        var output_index: usize = 0;
        for (self.outputs.items) |output| {
            if (output.removed) continue;
            outputs[output_index] = .{
                .id = output.id,
                .registry_name = output.registry_name,
                .name = output.name,
            };
            output_index += 1;
        }

        const keymaps = try allocator.alloc(policy.Keymap, self.keymaps.items.len);
        for (self.keymaps.items, keymaps) |source, *destination| destination.* = .{
            .id = source.id,
            .state = source.state,
            .error_message = source.error_message,
        };
        const accel_configs = try allocator.alloc(policy.AccelConfig, self.accel_configs.items.len);
        for (self.accel_configs.items, accel_configs) |source, *destination| destination.* = .{
            .id = source.id,
            .profile = source.profile,
        };
        return .{
            .devices = devices,
            .outputs = outputs,
            .keymaps = keymaps,
            .accel_configs = accel_configs,
            .events = self.events.items,
            .input_management_version = self.input_manager.?.getVersion(),
            .libinput_config_version = self.libinput_config.?.getVersion(),
            .xkb_config_version = self.xkb_config.?.getVersion(),
        };
    }

    fn validateCommands(self: *Runtime, allocator: std.mem.Allocator, commands: []const policy.Command) !void {
        var keymaps: std.StringHashMapUnmanaged(policy.KeymapState) = .empty;
        defer keymaps.deinit(allocator);
        for (self.keymaps.items) |keymap| try keymaps.put(allocator, keymap.id, keymap.state);
        var configs: std.StringHashMapUnmanaged(usize) = .empty;
        defer configs.deinit(allocator);
        for (self.accel_configs.items) |config| try configs.put(allocator, config.id, config.pending_results);

        for (commands) |command| {
            try validateScalarCommand(command);
            try validateAccelConfigCommand(&configs, allocator, command);
            if (baseDeviceId(command)) |id| _ = try self.requireDevice(id);
            if (keyboardDeviceId(command)) |id| _ = try self.requireKeyboard(id);
            if (libinputDeviceId(command)) |id| _ = try self.requireLibinput(id);
            switch (command) {
                .create_seat => |value| try validateWaylandString(value.name),
                .destroy_seat => |value| try validateWaylandString(value.name),
                .assign_to_seat => |value| {
                    try validateWaylandString(value.name);
                },
                .map_to_output => |value| {
                    if (value.output) |id| _ = try self.requireOutput(id);
                },
                .create_keymap => |value| {
                    try validateIdentifier(value.id);
                    if (std.mem.indexOfScalar(u8, value.text, 0) != null) return error.InvalidKeymapText;
                    if (keymaps.contains(value.id)) return error.DuplicateKeymap;
                    try keymaps.put(allocator, value.id, .pending);
                },
                .destroy_keymap => |value| {
                    if (!keymaps.remove(value.id)) return error.UnknownKeymap;
                },
                .set_keymap => |value| {
                    if ((keymaps.get(value.id) orelse return error.UnknownKeymap) != .ready)
                        return error.KeymapNotReady;
                },
                .set_layout_by_name => |value| {
                    try validateWaylandString(value.name);
                },
                .set_send_events => |value| {
                    if (value.mode > 2) return error.InvalidSendEventsMode;
                },
                else => {},
            }
        }
    }

    fn applyCommands(self: *Runtime, commands: []const policy.Command) !void {
        for (commands) |command| switch (command) {
            .create_seat => |value| {
                const name = try self.allocator.dupeZ(u8, value.name);
                defer self.allocator.free(name);
                self.input_manager.?.createSeat(name);
            },
            .destroy_seat => |value| {
                const name = try self.allocator.dupeZ(u8, value.name);
                defer self.allocator.free(name);
                self.input_manager.?.destroySeat(name);
            },
            .assign_to_seat => |value| {
                const name = try self.allocator.dupeZ(u8, value.name);
                defer self.allocator.free(name);
                self.findDeviceId(value.device).?.object.assignToSeat(name);
            },
            .set_repeat_info => |value| self.findDeviceId(value.device).?.object.setRepeatInfo(value.rate, value.delay),
            .set_scroll_factor => |value| self.findDeviceId(value.device).?.object.setScrollFactor(wl.Fixed.fromDouble(value.factor)),
            .map_to_output => |value| self.findDeviceId(value.device).?.object.mapToOutput(
                if (value.output) |id| self.findOutputId(id).?.object else null,
            ),
            .map_to_rectangle => |value| self.findDeviceId(value.device).?.object.mapToRectangle(
                value.x,
                value.y,
                value.width,
                value.height,
            ),
            .create_keymap => |value| try self.createKeymap(value),
            .destroy_keymap => |value| {
                const index = self.findKeymapIndex(value.id).?;
                const keymap = self.keymaps.orderedRemove(index);
                self.destroyKeymap(keymap, true);
            },
            .set_keymap => |value| self.findDeviceId(value.device).?.keyboard.?.object.setKeymap(self.findKeymap(value.id).?.object),
            .set_layout_by_index => |value| self.findDeviceId(value.device).?.keyboard.?.object.setLayoutByIndex(value.index),
            .set_layout_by_name => |value| {
                const name = try self.allocator.dupeZ(u8, value.name);
                defer self.allocator.free(name);
                self.findDeviceId(value.device).?.keyboard.?.object.setLayoutByName(name);
            },
            .set_capslock => |value| if (value.enabled)
                self.findDeviceId(value.device).?.keyboard.?.object.capslockEnable()
            else
                self.findDeviceId(value.device).?.keyboard.?.object.capslockDisable(),
            .set_numlock => |value| if (value.enabled)
                self.findDeviceId(value.device).?.keyboard.?.object.numlockEnable()
            else
                self.findDeviceId(value.device).?.keyboard.?.object.numlockDisable(),
            .create_accel_config => |value| try self.createAccelConfig(value.id, value.profile),
            .destroy_accel_config => |value| {
                const index = self.findAccelConfigIndex(value.id).?;
                const config = self.accel_configs.orderedRemove(index);
                self.destroyAccelConfig(config, true);
            },
            .set_accel_points => |value| {
                const config = self.findAccelConfig(value.id).?;
                var step = value.step;
                var step_array = waylandArray(f64, std.mem.asBytes(&step));
                var points_array = waylandArray(f64, std.mem.sliceAsBytes(value.points));
                const object = try config.object.setPoints(
                    @enumFromInt(@intFromEnum(value.type)),
                    &step_array,
                    &points_array,
                );
                try self.addResult(object, .{ .accel_config = config }, .set_accel_points);
            },
            .apply_accel_config => |value| {
                const object = try self.findDeviceId(value.device).?.libinput.?.object.applyAccelConfig(
                    self.findAccelConfig(value.id).?.object,
                );
                try self.addResult(object, .{ .device = value.device }, .apply_accel_config);
            },
            .set_send_events => |value| try self.addDeviceResult(value.device, .set_send_events, try self.findDeviceId(value.device).?.libinput.?.object.setSendEvents(@bitCast(value.mode))),
            .set_tap => |value| try self.addDeviceResult(value.device, .set_tap, try self.findDeviceId(value.device).?.libinput.?.object.setTap(enumCast(river.LibinputDeviceV1.TapState, value.state))),
            .set_tap_button_map => |value| try self.addDeviceResult(value.device, .set_tap_button_map, try self.findDeviceId(value.device).?.libinput.?.object.setTapButtonMap(enumCast(river.LibinputDeviceV1.TapButtonMap, value.map))),
            .set_drag => |value| try self.addDeviceResult(value.device, .set_drag, try self.findDeviceId(value.device).?.libinput.?.object.setDrag(enumCast(river.LibinputDeviceV1.DragState, value.state))),
            .set_drag_lock => |value| try self.addDeviceResult(value.device, .set_drag_lock, try self.findDeviceId(value.device).?.libinput.?.object.setDragLock(enumCast(river.LibinputDeviceV1.DragLockState, value.state))),
            .set_three_finger_drag => |value| try self.addDeviceResult(value.device, .set_three_finger_drag, try self.findDeviceId(value.device).?.libinput.?.object.setThreeFingerDrag(enumCast(river.LibinputDeviceV1.ThreeFingerDragState, value.state))),
            .set_calibration_matrix => |value| {
                var matrix = value.matrix;
                var array = waylandArray(f32, std.mem.asBytes(&matrix));
                try self.addDeviceResult(value.device, .set_calibration_matrix, try self.findDeviceId(value.device).?.libinput.?.object.setCalibrationMatrix(&array));
            },
            .set_accel_profile => |value| try self.addDeviceResult(value.device, .set_accel_profile, try self.findDeviceId(value.device).?.libinput.?.object.setAccelProfile(enumCast(river.LibinputDeviceV1.AccelProfile, value.profile))),
            .set_accel_speed => |value| {
                var speed = value.speed;
                var array = waylandArray(f64, std.mem.asBytes(&speed));
                try self.addDeviceResult(value.device, .set_accel_speed, try self.findDeviceId(value.device).?.libinput.?.object.setAccelSpeed(&array));
            },
            .set_natural_scroll => |value| try self.addDeviceResult(value.device, .set_natural_scroll, try self.findDeviceId(value.device).?.libinput.?.object.setNaturalScroll(binaryEnum(river.LibinputDeviceV1.NaturalScrollState, value.enabled))),
            .set_left_handed => |value| try self.addDeviceResult(value.device, .set_left_handed, try self.findDeviceId(value.device).?.libinput.?.object.setLeftHanded(binaryEnum(river.LibinputDeviceV1.LeftHandedState, value.enabled))),
            .set_click_method => |value| try self.addDeviceResult(value.device, .set_click_method, try self.findDeviceId(value.device).?.libinput.?.object.setClickMethod(enumCast(river.LibinputDeviceV1.ClickMethod, value.method))),
            .set_clickfinger_button_map => |value| try self.addDeviceResult(value.device, .set_clickfinger_button_map, try self.findDeviceId(value.device).?.libinput.?.object.setClickfingerButtonMap(enumCast(river.LibinputDeviceV1.ClickfingerButtonMap, value.map))),
            .set_middle_emulation => |value| try self.addDeviceResult(value.device, .set_middle_emulation, try self.findDeviceId(value.device).?.libinput.?.object.setMiddleEmulation(binaryEnum(river.LibinputDeviceV1.MiddleEmulationState, value.enabled))),
            .set_scroll_method => |value| try self.addDeviceResult(value.device, .set_scroll_method, try self.findDeviceId(value.device).?.libinput.?.object.setScrollMethod(enumCast(river.LibinputDeviceV1.ScrollMethod, value.method))),
            .set_scroll_button => |value| try self.addDeviceResult(value.device, .set_scroll_button, try self.findDeviceId(value.device).?.libinput.?.object.setScrollButton(value.button)),
            .set_scroll_button_lock => |value| try self.addDeviceResult(value.device, .set_scroll_button_lock, try self.findDeviceId(value.device).?.libinput.?.object.setScrollButtonLock(binaryEnum(river.LibinputDeviceV1.ScrollButtonLockState, value.enabled))),
            .set_dwt => |value| try self.addDeviceResult(value.device, .set_dwt, try self.findDeviceId(value.device).?.libinput.?.object.setDwt(binaryEnum(river.LibinputDeviceV1.DwtState, value.enabled))),
            .set_dwtp => |value| try self.addDeviceResult(value.device, .set_dwtp, try self.findDeviceId(value.device).?.libinput.?.object.setDwtp(binaryEnum(river.LibinputDeviceV1.DwtpState, value.enabled))),
            .set_rotation => |value| try self.addDeviceResult(value.device, .set_rotation, try self.findDeviceId(value.device).?.libinput.?.object.setRotation(value.angle)),
        };
    }

    fn createKeymap(self: *Runtime, value: anytype) !void {
        const fd = try createSealedKeymapFd(value.text);
        defer _ = linux.close(fd);
        const object = try self.xkb_config.?.createKeymap(fd, switch (value.format) {
            .text_v1 => .text_v1,
            .text_v2 => .text_v2,
        });
        errdefer object.destroy();
        const keymap = try self.allocator.create(Keymap);
        errdefer self.allocator.destroy(keymap);
        const id = try self.allocator.dupe(u8, value.id);
        errdefer self.allocator.free(id);
        keymap.* = .{ .runtime = self, .object = object, .id = id };
        object.setListener(*Keymap, keymapListener, keymap);
        try self.keymaps.append(self.allocator, keymap);
    }

    fn createAccelConfig(self: *Runtime, id_value: []const u8, profile: policy.AccelProfile) !void {
        const object = try self.libinput_config.?.createAccelConfig(enumCast(river.LibinputDeviceV1.AccelProfile, profile));
        errdefer object.destroy();
        const config = try self.allocator.create(AccelConfig);
        errdefer self.allocator.destroy(config);
        const id = try self.allocator.dupe(u8, id_value);
        errdefer self.allocator.free(id);
        config.* = .{ .runtime = self, .object = object, .id = id, .profile = profile };
        try self.accel_configs.append(self.allocator, config);
    }

    fn addDeviceResult(self: *Runtime, device: u32, operation: policy.Operation, object: *river.LibinputResultV1) !void {
        try self.addResult(object, .{ .device = device }, operation);
    }

    fn addResult(self: *Runtime, object: *river.LibinputResultV1, target: ResultTarget, operation: policy.Operation) !void {
        errdefer object.destroy();
        const result = try self.allocator.create(Result);
        errdefer self.allocator.destroy(result);
        result.* = .{ .runtime = self, .object = object, .target = target, .operation = operation };
        switch (target) {
            .accel_config => |config| config.pending_results += 1,
            .device => {},
        }
        errdefer switch (target) {
            .accel_config => |config| config.pending_results -= 1,
            .device => {},
        };
        object.setListener(*Result, resultListener, result);
        try self.results.append(self.allocator, result);
    }

    fn sweepRemoved(self: *Runtime) void {
        var keyboard_index: usize = 0;
        while (keyboard_index < self.keyboards.items.len) {
            const keyboard = self.keyboards.items[keyboard_index];
            if (!keyboard.removed and (keyboard.device == null or !keyboard.device.?.removed)) {
                keyboard_index += 1;
                continue;
            }
            _ = self.keyboards.orderedRemove(keyboard_index);
            if (keyboard.device) |device| if (device.keyboard == keyboard) {
                device.keyboard = null;
            };
            self.destroyKeyboard(keyboard, true);
        }
        var libinput_index: usize = 0;
        while (libinput_index < self.libinput_devices.items.len) {
            const device = self.libinput_devices.items[libinput_index];
            if (!device.removed and (device.device == null or !device.device.?.removed)) {
                libinput_index += 1;
                continue;
            }
            _ = self.libinput_devices.orderedRemove(libinput_index);
            if (device.device) |base| if (base.libinput == device) {
                base.libinput = null;
            };
            self.destroyLibinputDevice(device, true);
        }
        var device_index: usize = 0;
        while (device_index < self.devices.items.len) {
            const device = self.devices.items[device_index];
            if (!device.removed) {
                device_index += 1;
                continue;
            }
            _ = self.devices.orderedRemove(device_index);
            self.destroyDevice(device, true);
        }
        var output_index: usize = 0;
        while (output_index < self.outputs.items.len) {
            const output = self.outputs.items[output_index];
            if (!output.removed) {
                output_index += 1;
                continue;
            }
            _ = self.outputs.orderedRemove(output_index);
            self.destroyOutput(output, true);
        }
    }

