    fn eventLoopPrepare(ctx: *anyopaque) !event_loop.EventLoop.WaylandPrepare {
        const self: *Runtime = @ptrCast(@alignCast(ctx));
        var dispatched_pending = false;
        while (!self.display.prepareRead()) {
            if (self.display.dispatchPending() != .SUCCESS) return error.DispatchFailed;
            dispatched_pending = true;
        }
        const events: u32 = switch (self.display.flush()) {
            .SUCCESS => linux.EPOLL.IN,
            .AGAIN => linux.EPOLL.IN | linux.EPOLL.OUT,
            else => {
                self.display.cancelRead();
                return error.FlushFailed;
            },
        };
        return .{ .events = events, .dispatched_pending = dispatched_pending };
    }

    fn eventLoopFinish(ctx: *anyopaque, events: u32) !bool {
        const self: *Runtime = @ptrCast(@alignCast(ctx));
        if (events & linux.EPOLL.IN != 0) {
            if (self.display.readEvents() != .SUCCESS) return error.ReadEventsFailed;
        } else {
            self.display.cancelRead();
        }
        if (self.display.dispatchPending() != .SUCCESS) return error.DispatchFailed;
        return !self.shouldStop();
    }

    fn registryListener(registry: *wl.Registry, event: wl.Registry.Event, self: *Runtime) void {
        switch (event) {
            .global => |global| {
                const interface = std.mem.span(global.interface);
                if (std.mem.eql(u8, interface, std.mem.span(river.InputManagerV1.interface.name))) {
                    if (self.input_manager != null) return fatal(self);
                    const object = registry.bind(global.name, river.InputManagerV1, @min(global.version, river.InputManagerV1.generated_version)) catch return fatal(self);
                    self.input_manager = object;
                    self.input_global = global.name;
                    object.setListener(*Runtime, inputManagerListener, self);
                } else if (std.mem.eql(u8, interface, std.mem.span(river.LibinputConfigV1.interface.name))) {
                    if (self.libinput_config != null) return fatal(self);
                    const object = registry.bind(global.name, river.LibinputConfigV1, @min(global.version, river.LibinputConfigV1.generated_version)) catch return fatal(self);
                    self.libinput_config = object;
                    self.libinput_global = global.name;
                    object.setListener(*Runtime, libinputConfigListener, self);
                } else if (std.mem.eql(u8, interface, std.mem.span(river.XkbConfigV1.interface.name))) {
                    if (self.xkb_config != null) return fatal(self);
                    const object = registry.bind(global.name, river.XkbConfigV1, @min(global.version, river.XkbConfigV1.generated_version)) catch return fatal(self);
                    self.xkb_config = object;
                    self.xkb_global = global.name;
                    object.setListener(*Runtime, xkbConfigListener, self);
                } else if (std.mem.eql(u8, interface, std.mem.span(wl.Output.interface.name))) {
                    self.addOutput(registry, global.name, global.version) catch return fatal(self);
                }
            },
            .global_remove => |removed| {
                if (self.input_global == removed.name or self.libinput_global == removed.name or self.xkb_global == removed.name)
                    return fatal(self);
                const output = self.findOutputRegistryName(removed.name) orelse return;
                if (output.removed) return;
                output.removed = true;
                self.recordEvent(.{ .output_removed = output.id });
            },
        }
    }

    fn addOutput(self: *Runtime, registry: *wl.Registry, name: u32, version: u32) !void {
        const object = try registry.bind(name, wl.Output, @min(version, wl.Output.generated_version));
        errdefer if (object.getVersion() >= wl.Output.release_since_version) object.release() else object.destroy();
        const output = try self.allocator.create(Output);
        errdefer self.allocator.destroy(output);
        output.* = .{
            .runtime = self,
            .id = try self.allocateId(),
            .registry_name = name,
            .object = object,
        };
        object.setListener(*Output, outputListener, output);
        try self.outputs.append(self.allocator, output);
        self.recordEvent(.{ .output_added = output.id });
    }

