fn decodeArray(comptime T: type, array: *wl.Array) ?T {
    if (array.size != @sizeOf(T) or array.data == null) return null;
    var value: T = undefined;
    const source: [*]const u8 = @ptrCast(array.data.?);
    @memcpy(std.mem.asBytes(&value), source[0..@sizeOf(T)]);
    return value;
}

fn createSealedKeymapFd(text: []const u8) !i32 {
    const fd = try std.posix.memfd_create("keywork-river-keymap", linux.MFD.CLOEXEC | linux.MFD.ALLOW_SEALING);
    errdefer _ = linux.close(fd);
    const length = std.math.add(usize, text.len, 1) catch return error.KeymapTooLarge;
    if (linux.errno(linux.ftruncate(fd, @intCast(length))) != .SUCCESS) return error.KeymapFdFailed;
    try writeAllFd(fd, text);
    try writeAllFd(fd, "\x00");
    const seals = linux.F.SEAL_WRITE | linux.F.SEAL_GROW | linux.F.SEAL_SHRINK | linux.F.SEAL_SEAL;
    const result = linux.fcntl(fd, linux.F.ADD_SEALS, seals);
    if (linux.errno(result) != .SUCCESS) return error.KeymapSealFailed;
    return fd;
}

fn writeAllFd(fd: i32, data: []const u8) !void {
    var written: usize = 0;
    while (written < data.len) {
        const result = linux.write(fd, data.ptr + written, data.len - written);
        switch (linux.errno(result)) {
            .SUCCESS => written += result,
            .INTR => continue,
            else => return error.KeymapWriteFailed,
        }
    }
}

test "scalar validation rejects protocol-fatal values" {
    try std.testing.expectError(error.InvalidRepeatInfo, validateScalarCommand(.{
        .set_repeat_info = .{ .device = 1, .rate = -1, .delay = 0 },
    }));
    try std.testing.expectError(error.InvalidRotation, validateScalarCommand(.{
        .set_rotation = .{ .device = 1, .angle = 360 },
    }));
    try std.testing.expectError(error.InvalidAccelPoints, validateScalarCommand(.{
        .set_accel_points = .{ .id = "a", .type = .motion, .step = 1, .points = &.{} },
    }));
}

test "accel config cannot be destroyed after creating a result in the same batch" {
    const allocator = std.testing.allocator;
    var configs: std.StringHashMapUnmanaged(usize) = .empty;
    defer configs.deinit(allocator);
    try configs.put(allocator, "curve", 0);
    try validateAccelConfigCommand(&configs, allocator, .{ .set_accel_points = .{
        .id = "curve",
        .type = .motion,
        .step = 1,
        .points = &.{1},
    } });
    try std.testing.expectError(error.AccelConfigInUse, validateAccelConfigCommand(
        &configs,
        allocator,
        .{ .destroy_accel_config = .{ .id = "curve" } },
    ));
}
