            },
            .failure => |value| {
                replaceString(keymap.runtime.allocator, &keymap.error_message, value.error_msg) catch return fatal(keymap.runtime);
                keymap.state = .failed;
                keymap.runtime.recordEvent(.{ .keymap_failed = .{
